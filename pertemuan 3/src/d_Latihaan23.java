public class d_Latihaan23{
    public static void main(String[] args) {
        int A = 7;
        // increment
        System.out.println("A : " + A);
        System.out.println("++A : " + ++A);
        System.out.println("A++ : " + A++);
        // decrement
        System.out.println("--A : " + --A);
        System.out.println("A--: " + A--);
        System.out.println("A : " + A);

    }
}