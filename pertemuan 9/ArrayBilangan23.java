public class ArrayBilangan23 {
    public static void main(String[] args) {
        
    // array bertipe integer dengan kapasitas 4 elemen
    int [] bil = new int [4];
    bil [0] = 5;
    bil [1] = 13;
    bil [2] = -7;
    bil [3] = 17;

    // inisialisasi elemen array sekaligus pada saat deklarasi array.
    for (int i = 0; i <=  4; i++) {
        System.out.println(bil[i]);
    }
    }
}
